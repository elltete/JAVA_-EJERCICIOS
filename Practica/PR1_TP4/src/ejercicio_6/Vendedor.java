package ejercicio_6;

public class Vendedor {

	private int idVendedor;
	private String nombre;
	
	public Vendedor(int idVendedor, String nombre) {
		this.idVendedor = idVendedor;
		this.nombre = nombre;
	}

	public int getIdVendedor() {
		return idVendedor;
	}

	public String getNombre() {
		return nombre;
	}
	
}
